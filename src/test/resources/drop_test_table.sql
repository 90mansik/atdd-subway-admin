DROP TABLE STATION;
DROP TABLE LINE;

CREATE TABLE STATION (ID BIGINT GENERATED BY DEFAULT AS IDENTITY, CREATED_DATE TIMESTAMP, MODIFIED_DATE TIMESTAMP, NAME VARCHAR(255), LINE_ID BIGINT, PRIMARY KEY (ID));
CREATE TABLE LINE (ID BIGINT GENERATED BY DEFAULT AS IDENTITY, CREATED_DATE TIMESTAMP, MODIFIED_DATE TIMESTAMP, COLOR VARCHAR(255), DELETED BOOLEAN NOT NULL, NAME VARCHAR(255), PRIMARY KEY (ID));
